let num1 = 100;
let num2 = 200;

function ifelse() {
  if (num1 > num2) {
    console.log("\tNumber 1 is less than Number 2");
  } else if (num1 < num2) {
    console.log("\tNumber 2 is greater than Number 1");
  } else if (num1 == num2) {
    console.log("\tNumber 2 is equal to Number 1");
  } else {
    console.log("\tNAN");
  }
}
ifelse();





